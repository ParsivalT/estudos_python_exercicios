
def dobro(valor):
    '''Recebe um valor e multiplica o mesmo por 2'''

    return valor * 2 

def metade(valor):
    '''Recebe um valor e retorna a metade'''

    return valor / 2

def aumenta(valor):
    '''Recebe um valor e retorna o mesmo
       com um aumento de 10%'''

    return valor + ((valor * 10) / 100)

def diminui(valor):
    '''Recebe um valor e retorna o mesmo
       com  10% menor '''

    return valor - ((valor * 10) / 100)

    